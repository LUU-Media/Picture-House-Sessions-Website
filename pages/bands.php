    <h3>Laminate Pet Animal</h3>
    <a href="https://www.facebook.com/pg/laminatepetanimalmusic">Facebook Page</a>
    <p>You may hear hints of Gold Panda, The Japanese House and Bonobo in Laminate Pet Animal; but the electronica trio from Leeds (UK), have quickly fabricated their own sound with organic samples, lush vocals and pulsing bass lines.</p>
    <p>Having forged a reputation through remixing established artists such as Magic Man and many local bands, LPA soon found themselves getting booked to support Gold Panda, Emancipator, Slow Magic and Nathan Fake, as well as playing multiple festivals including Live At Leeds and Sound City.</p>
